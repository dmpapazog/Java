
public class math {

    public static void main(String[] args) {
        // Math functions
        System.out.println("abs(-1) = " + Math.abs(-1) + '\n');

        
        System.out.println("ceil(4.5)  = " + Math.ceil(4.5));
        System.out.println("floor(4.5) = " + Math.floor(4.5));
        System.out.println("round(4.5) = " + Math.round(4.5) + '\n');

        System.out.println("max(4, 5) = " + Math.max(4, 5));
        System.out.println("min(4, 5) = " + Math.min(4, 5) + '\n');

        System.out.println("exp(1)   = " + Math.exp(1));
        System.out.println("log(e)   = " + Math.log(Math.E));
        System.out.println("log10(1) = " + Math.log10(1) + '\n');

        System.out.println("pow(2, 3)  = " + Math.pow(2, 3));
        System.out.println("sqrt(4)    = " + Math.sqrt(4));
        System.out.println("cbrt(8)    = " + Math.cbrt(8) + '\n');

        System.out.println("hypot(4, 3) = " + Math.hypot(4, 3) + '\n');

        System.out.println("PI = " + Math.PI + '\n');

        // Trigonometric functions
        System.out.println("toDegrees(PI / 2) = " + Math.toDegrees(Math.PI / 2));
        System.out.println("toRadians(90)     = " + Math.toRadians(90));
        System.out.println("And all trigonometric functions." + '\n');

    }

}